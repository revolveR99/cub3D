int	ft_islower(int c)
{
	return (c >= 97 && c <= 122);
}

int	ft_toupper(int c)
{
	if (ft_islower(c))
		return (c - 32);
	else
	{
		return (c);
		c = c - 32;
	}
	return (0);
}
